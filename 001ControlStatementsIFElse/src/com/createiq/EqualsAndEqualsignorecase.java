package com.createiq;

public class EqualsAndEqualsignorecase {

	public static void main(String[] args) {
		String input = "rose";
		 
		if(input.equals("rose")) {
			System.out.println("Jasmine");
		}
		if(input.equals("marigold")) {
			System.out.println("lily");
		}
		if(input.equals("tulip")) {
			System.out.println("sunflower");
		}
		if(input.equals("lotus")) {
			System.out.println("daisy");
		}
		if(input.equals("hibiscus")) {
			System.out.println("primrose");
		}
		if(input.equals("orchid")) {
			System.out.println("rohira");
		}

	}

}
