package vectorAndStackBased;

public class section {
      String s1;
      int Strenth;
      double fees;
	public section(String s1, int Strenth, double fees) {
		this.s1=s1;
		this.Strenth=Strenth;
		this.fees=fees;
	}
	@Override
	public String toString() {
		return "section [s1=" + s1 + ", Strenth=" + Strenth + ", fees=" + fees + "]";
	}

	
	

}
